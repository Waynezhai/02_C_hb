#include <stdio.h>

int main(void)
{
	int i;
	int j;
	int m, k;

	i = j = 4;
	k = i++;
	m = ++j;

	printf("i = %d; j = %d; m = %d; k =%d\n", i, j, m, k);

	return 0;
}


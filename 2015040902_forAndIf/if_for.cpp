#include <stdio.h>

int main(void)
{
	int i;
	int sum = 0;

	for (i=1; i<100; i++)
	{
		if (i%3 == 0)
		{
			sum += i;
			printf("%d\t", i);
			if(i%9 == 0)
			{
				printf("sum = %d", sum);
				printf("\n");
			}
		}
	}
	//printf("sum = %d\n", sum);

	return 0;
}


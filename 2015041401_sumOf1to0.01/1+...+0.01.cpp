#include <stdio.h>

int main(void)
{
	int n;
	double sum = 0;

	for (n=1; n<=100; n++)
	{
		sum += 1.0/n;
		//printf("sum = %f\n", sum);
	}
	
	printf("sum = %f\n", sum);

	return 0;
}


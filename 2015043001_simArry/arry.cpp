#include <stdio.h>

int main(void)
{
	int i = 0;
	int a[5] = {0, 1, 2, 3, 4};

	for (i=0; i<=4; i++)
	{
		printf("a[%d] = %d\n", i, a[i]);
	}

	return 0;
}

